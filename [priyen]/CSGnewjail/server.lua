	local col = createColTube(1,1,60)
	attachElements(col,ped)
	addEventHandler("onColShapeHit",col,function(e)
		if getElementType(e) == "player" then
			giveWeapon(ped,31,99999)
			setPedWeaponSlot(ped,5)
		end
