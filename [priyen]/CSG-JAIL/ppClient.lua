function calcReleaseTime()
	wp = getElementData(localPlayer,"wantedPoints")
	local secs=wp*unit
	local daysUp=0

	local gHours=secs/60
	gHours=math.floor(gHours)

	local day = exports.csgtime:getDay()
	local id = dayToID[day]
	local gDays=math.floor(gHours/24)
	local gHours=gHours-(gDays*24)
	local h,m = getTime()

	while h+gHours > 23 do
		gHours=gHours-(24-h)
	end
	secs = secs-(24*gDays*60)-(60*gHours)
	if secs+m > 60 then
		secs=(secs+m)-60
		gHours=gHours+1
		m=0
	end
	local newid=id+gDays
	if newid>7 then newid=newid-7 end
	for k,v in pairs(dayToID) do
		if v == newid then
			newday=k
			break
		end
	end
	while h+gHours > 23 do
		gHours=gHours-(24-h)
		gDays=gDays+1
		if secs+m > 60 then
			secs=(secs+m)-60
			gHours=gHours+1
			m=0
		end
	end
	if secs+m > 60 then
		secs=(secs+m)-60
		gHours=gHours+1
		m=0
	end
	while h+gHours > 23 do
		gHours=gHours-(24-h)
		gDays=gDays+1
		if secs+m > 60 then
			secs=(secs+m)-60
			gHours=gHours+1
			m=0
		end
	end
	local mstr=math.floor(m+secs)
	local hstr=h+gHours
	if mstr < 10 then
		mstr = "0"..mstr..""
	end
	if hstr < 10 then
		hstr = "0"..hstr..""
	end
	releaseString=newday.." at "..h+gHours..":"..mstr..""
end

